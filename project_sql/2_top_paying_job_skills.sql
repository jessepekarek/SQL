/*
**Question: What skills are required for the top-paying 
data-analyst jobs?** 

- Use the top 10 highest-paying Data Analyst jobs from first query 

- Add the specific skills required for these roles
- Filters for roles with specified salaries that are remote
- Why? It provides a detailed look at which high-paying jobs 
demand certain skills, helping job seekers understand which 
skills to develop that align with top salaries
*/
WITH top_paying_jobs AS (
SELECT
    jpf.job_id,
    jpf.job_title,
    jpf.salary_year_avg,
    cd.name AS company_name
FROM 
    job_postings_fact jpf
LEFT JOIN company_dim cd ON cd.company_id = jpf.company_id
WHERE
    job_title_short = 'Data Analyst' AND
    job_location = 'Anywhere' AND
    salary_year_avg IS NOT NULL AND
    job_id IN (SELECT job_id FROM skills_job_dim)
ORDER BY
    salary_year_avg DESC
    LIMIT 10
)
SELECT 
    tpj.*,
    sd.skills
FROM top_paying_jobs tpj
INNER JOIN skills_job_dim sjd ON tpj.job_id = sjd.job_id
INNER JOIN skills_dim sd ON sd.skill_id = sjd.skill_id
ORDER BY
    salary_year_avg DESC

